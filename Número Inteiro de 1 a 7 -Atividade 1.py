dia = int(input("Coloque um número de 1 a 7 para achar o dia da semana: ","\n"))

if dia == 1:
  print("Domingo","\n")
elif dia == 2:
  print("Segunda","\n")
elif dia == 3:
  print("terça","\n")
elif dia == 4:
  print("quarta","\n")
elif dia == 5:
  print("quinta","\n")
elif dia == 6:
  print("sexta","\n")
elif dia == 7:
  print("sábado","\n")
else:
  print("erro","\n")
